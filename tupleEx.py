# example of Tuple  - tupleEx.py
# Tuple is similar like a List, with differences with that it can't be changed once defined, the values and size remains unchanged
# Also, a Tuple is defined with normal brackets, unlike Lists are defined with Square Brackets
a = (24, '1bc', 2+2j, "example with some values")
print("here is the example of a Tuple", a)

print("Individual values can be printed like", a[1])
